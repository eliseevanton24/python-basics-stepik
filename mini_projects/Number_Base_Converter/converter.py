n = int(input("Введите десятичное число: "))

print(f"Двоичная (2):      {bin(n)[2:]}")    # bin() → '0b1111111'
print(f"Восьмеричная (8):  {oct(n)[2:]}")    # oct() → '0o377'
print(f"Шестнадцатеричная: {hex(n)[2:].upper()}")    # hex() → '0xff'

# Обратно в десятичную — int(строка, основание)
b = input("\nВведите двоичное число (без префикса): ")
print(f"Двоичное {b} → десятичное: {int(b, 2)}")

o = input("Введите восьмеричное число (без префикса): ")
print(f"Восьмеричное {o} → десятичное: {int(o, 8)}")

h = input("Введите шестнадцатеричное число (без префикса): ")
print(f"Шестнадцатеричное {h} → десятичное: {int(h, 16)}")